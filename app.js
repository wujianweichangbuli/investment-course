function formatEuro(value) {
  return `${Math.round(value).toLocaleString("zh-CN")} 欧`;
}

function bindCashflowCalculator() {
  const stableIncome = document.querySelector("#stableIncome");
  const necessaryExpense = document.querySelector("#necessaryExpense");
  const reserveNeed = document.querySelector("#reserveNeed");
  const result = document.querySelector("#cashflowResult");

  function calculate() {
    state.calculators.cashflow.stableIncome = Number(stableIncome.value || 0);
    state.calculators.cashflow.necessaryExpense = Number(necessaryExpense.value || 0);
    state.calculators.cashflow.reserveNeed = Number(reserveNeed.value || 0);
    saveState();
    const disposable = state.calculators.cashflow.stableIncome - state.calculators.cashflow.necessaryExpense;
    const investable = disposable - state.calculators.cashflow.reserveNeed;
    const verdict =
      investable > 0
        ? "有理论可投资余额，但仍应先确认应急金是否达标。"
        : "不建议进入真实投资阶段，应优先改善现金流或补足储备。";
    result.innerHTML = `
      <strong>可支配收入：${formatEuro(disposable)}</strong>
      <p>保守可投资金额：${formatEuro(investable)}</p>
      <p>${verdict}</p>
    `;
  }

  [stableIncome, necessaryExpense, reserveNeed].forEach((input) => {
    input.addEventListener("input", calculate);
  });
  calculate();
}

function bindEmergencyCalculator() {
  const monthlyMinimum = document.querySelector("#monthlyMinimum");
  const currentEmergency = document.querySelector("#currentEmergency");
  const result = document.querySelector("#emergencyResult");

  function calculate() {
    state.calculators.emergency.monthlyMinimum = Number(monthlyMinimum.value || 0);
    state.calculators.emergency.currentEmergency = Number(currentEmergency.value || 0);
    saveState();
    const monthly = state.calculators.emergency.monthlyMinimum;
    const current = state.calculators.emergency.currentEmergency;
    const goal3 = monthly * 3;
    const goal6 = monthly * 6;
    const goal9 = monthly * 9;
    result.innerHTML = `
      <strong>3 个月目标：${formatEuro(goal3)}</strong>
      <p>6 个月目标：${formatEuro(goal6)}</p>
      <p>9 个月目标：${formatEuro(goal9)}</p>
      <p>距离 6 个月目标还差：${formatEuro(Math.max(goal6 - current, 0))}</p>
    `;
  }

  [monthlyMinimum, currentEmergency].forEach((input) => {
    input.addEventListener("input", calculate);
  });
  calculate();
}

function bindDrawdownCalculator() {
  const lossPercent = document.querySelector("#lossPercent");
  const result = document.querySelector("#drawdownResult");

  function calculate() {
    state.calculators.drawdown.lossPercent = Number(lossPercent.value || 0);
    saveState();
    const loss = Math.min(Math.max(state.calculators.drawdown.lossPercent, 0), 99);
    const recovery = loss >= 100 ? Infinity : (loss / (100 - loss)) * 100;
    result.innerHTML = `
      <strong>亏损 ${loss.toFixed(0)}% 后，需要上涨 ${recovery.toFixed(1)}% 才能回本</strong>
      <p>亏损越大，回本所需涨幅会非线性增加。</p>
    `;
  }

  lossPercent.addEventListener("input", calculate);
  calculate();
}

