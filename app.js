function escapeHtml(value) {
  return String(value)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#039;");
}

function renderNav() {
  lessonNav.innerHTML = lessons
    .map((lesson) => {
      const active = lesson.id === state.activeLessonId ? "active" : "";
      const current = lesson.id === state.currentLessonId ? "current" : "";
      const complete = state.completed[lesson.id] ? "complete" : "";
      return `
        <button class="lesson-button ${active} ${current} ${complete}" type="button" data-lesson="${lesson.id}">
          <span class="status-dot" aria-hidden="true"></span>
          <span>
            <strong>${lesson.label}</strong>
            <span>${escapeHtml(lesson.title)}</span>
          </span>
        </button>
      `;
    })
    .join("");
}

